
public class MultimediaItem {
	
	private String content;
	private int type;
	
	public MultimediaItem(String newContent, int newType) {
		this.content = newContent;
		this.type = newType;
	}
	
	public String getContent() {
		return this.content;
	}
	
	public int getType() {
		return this.type;
	}
	
}
